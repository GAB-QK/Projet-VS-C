void shell_Sort(int a[], int s)
{
    int gap, i;
    for (gap = s / 2; gap > 0; gap /= 2)
    {
        for (i = gap; i < s; i += 1)
        {
            int temp = a[i];
            int j;
            for (j = i; j >= gap && a[j - gap] > temp; j -= gap)
            {
                a[j] = a[j - gap];
            }
            a[j] = temp;
        }
    }
}
